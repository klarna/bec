%%==============================================================================
%% Type definition for the Group data structure
%%==============================================================================
-module(bec_group_t).

%%==============================================================================
%% Exports
%%==============================================================================
-export([ from_map/1, to_map/1]).

%%==============================================================================
%% Types
%%==============================================================================
-type name() :: binary().

%%==============================================================================
%% Export Types
%%==============================================================================
-export_type([ name/0 ]).

%%==============================================================================
%% API
%%==============================================================================
-spec from_map(map()) -> name().
from_map(#{ <<"name">> := Name }) ->
  Name.

-spec to_map(name()) -> map().
to_map(Name) ->
  #{ <<"name">> => Name }.
